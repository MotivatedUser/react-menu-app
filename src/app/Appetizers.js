class AppetizerMenu {
    constructor(
        name, 
        type,
        calories,
        price,
        image,
        disclaimer
    ) {
        this.name = name;
        this.type = type;
        this.calories = calories;
        this.price = price;
        this.image = image;
        this.disclaimer = disclaimer;
    }
}

export default AppetizerMenu;