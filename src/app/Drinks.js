class MenuDrink {
    constructor(
        id,
        name, 
        type,
        calories,
        price,
        image
    ) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.calories = calories;
        this.price = price;
        this.image = image;
    }
}

export default MenuDrink;